package day2;

public class Logical_Operator {

	public static void main(String[] args) {
		// int
		
		int a=2,b=5,c=6;
		System.out.println(a<b||a>c);
		System.out.println(a<b&&a>c);
		System.out.println(!(a<b));
		
		//byte
		
		byte aa=22,bb=25,cc=36;
		System.out.println(aa<bb||aa>cc);
		System.out.println(aa<bb&&aa>cc);
		System.out.println(!(aa<bb));
		
		
		//short
		
		short as=2,bs=5,cs=6;
		System.out.println(as<bs||as>cs);
		System.out.println(as<bs&&as>cs);
		System.out.println(!(as<bs));
		
		//long 
		
		long al=2,bl=5,cl=6;
		System.out.println(al<bl||al>cl);
		System.out.println(al<bl&&al>cl);
		System.out.println(!(al<bl));
		
		
		//double
		
		double ad=2.2,bd=5.3,cd=6.4;
		System.out.println(ad<bd||ad>cd);
		System.out.println(ad<bd&&ad>cd);
		System.out.println(!(ad<bd));
		
		
		//float 
		
		float  af=2.2f,bf=5.5f,cf=6.6f;
		System.out.println(af<bf||af>cf);
		System.out.println(af<bf&&af>cf);
		System.out.println(!(af<bf));
		
		
		//boolean 
		
		boolean abl=true,bbl=false;
		System.out.println(abl||bbl);
		System.out.println(abl && bbl);
		System.out.println(!(abl|bbl));
		
		
		 
		
		
		
	}

}
